export async function callFreeLLMAPI(
  env: AppEnv,
  messages: ChatMessage[],
  options?: { stream?: boolean }
): Promise<AgentChatResponse> {
  const baseUrl = env.FREELLMAPI_URL || 'http://localhost:3001'
  const apiKey = env.FREELLMAPI_KEY

  if (!apiKey) {
    return {
      content: '⚠️ FreeLLMAPI ключ не налаштовано. Додайте FREELLMAPI_KEY в .env файл.',
      provider: 'none',
    }
  }

  const response = await fetch(`${baseUrl}/v1/chat/completions`, {
    method: 'POST',
    headers: {
      'Content-Type': 'application/json',
      'Authorization': `Bearer ${apiKey}`,
    },
    body: JSON.stringify({
      model: 'auto',
      messages: messages.map(m => ({ role: m.role, content: m.content })),
      stream: options?.stream ?? false,
      temperature: 0.7,
      max_tokens: 1024,
    }),
  })

  if (!response.ok) {
    const errorText = await response.text()
    throw new Error(`FreeLLMAPI error ${response.status}: ${errorText}`)
  }

  const data = await response.json()
  const provider = response.headers.get('x-routed-via') || 'unknown'

  return {
    content: data.choices?.[0]?.message?.content || 'Не вдалося отримати відповідь.',
    provider,
  }
}
